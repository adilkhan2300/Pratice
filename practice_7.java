//The Do/While Loop exapmle
public class Main {
  public static void main(String[] args) {
    int i = 0;
    do {
      System.out.println(i);
      i++;
    }
    while (i <= 20);  
  }
}

//the while loop, we have created a simple "countdown" program
public class Main{
  public static void main(String[] args)
  {
    int countdown = 10;
    while (countdown>0){
      System.out.println(countdown);
      countdown--;
    }
    System.out.println(" happy new year");
  }
}

  
